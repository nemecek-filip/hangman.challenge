import UIKit

extension UIColor {
    static var background: UIColor {
        return UIColor(named: "BackgroundColor")!
    }
    
    static var accent: UIColor {
        return UIColor(named: "Accent")!
    }
    
    static var complementary: UIColor {
        return UIColor(named: "Complementary")!
    }
}
